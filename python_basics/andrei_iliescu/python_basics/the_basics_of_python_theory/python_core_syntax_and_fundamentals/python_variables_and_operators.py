### Variabile și operatori
# Variabilă: nume simbolic care ține o valoare. Atribuirea se face cu =.
# Regulile de nume: litere, cifre, underscore; nu începe cu cifră; sensibil la majuscule.
# Exemplu variabile și atribuire:
numar = 10
text = "Python"
numar = numar + 5  # reasignare
print(numar, text)

# Python suportă operanzi și operatori:
# +, -, *, /, //, %, ** pentru aritmetică
# ==, !=, <, >, <=, >= pentru comparare
# +=, -=, etc. pentru asignare combinată
# and, or, not pentru logici booleene
# + și * pentru șiruri: concatenare și repetare
s1 = "Bun"
s2 = "ă"
print(s1 + s2)      # "Bună"
print(s1 * 3)       # "BunBunBun"
print(5 > 3, 5 == 5)  # True True
