### Funcții
# Definirea unei funcții cu def și bloc indentat. Poate avea parametri și return.
# Exemplu 1:
def salut():
    print("Salutări din funcție!")
salut()


# Exemplu 2 cu parametri și return:
def suma(a, b):
    """Returnează suma lui a și b."""
    return a + b
rezultat = suma(3, 4)
print(f"Suma: {rezultat}")
